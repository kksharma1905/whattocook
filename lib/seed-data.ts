function toEntries(rows: Row[], category: Category): SeedEntry[] {
  return rows.map(([name, tags, cookTime, difficulty]) => ({
    name,
    category,
    tags,
    cookTime,
    difficulty,
    imageUrl: MEAL_IMAGES[name] ?? null,
  }));
}

function bothCategories(rows: Row[]): SeedEntry[] {
  return [...toEntries(rows, "LUNCH"), ...toEntries(rows, "DINNER")];
}

